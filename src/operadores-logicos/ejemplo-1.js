// AND, todas las condiciones se tienen que cumplir
if (4 == "4" && 5 ===5) {
    console.log("La condicion AND se cumplio");
} else {
    console.log("La condicion AND no se cumpliop");
}

// OR, una u otra condicion se tiene que cumplir
if (4 == "4" || 5 ===5) {
    console.log("La condicion OR se cumplio");
} else {
    console.log("La condicion OR no se cumpliop");
}
